package com.nbp.nbp.models;

public class PostType {
	private Integer id;
	private String type;

	public PostType() {}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}